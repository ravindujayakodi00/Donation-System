const mongoose = require('mongoose');

const requestSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    }

},{timestamps: true});

const Request = mongoose.model('Request', requestSchema);

module.exports = Request;

